    public String checkAccount(String user, String password){
        String kind = "";
        Connection ketNoi = DatVeXe.layKetNoi();
        String sql = "select Kind from Account where Account = '"+user+"' and Password = '"+password+"'";
        try {
            PreparedStatement ps = ketNoi.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            if (!rs.next()) return kind = "none";
            kind = rs.getString("Kind");
        } catch (Exception e) {
            System.out.println("Loi checkAccount! "+ e);
        }
        return kind;
    }
    
    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        String userName = txt_user.getText();
        String password = String.valueOf(txt_password.getPassword());
        if (userName.equals("")){
            label_err1.setForeground(Color.red);
            label_err1.setText("Tên đăng nhập không được bỏ trống!");
        }
        if (password.equals("")){
            label_err2.setForeground(Color.red);
            label_err2.setText("mật khẩu không được bỏ trống!");
        }
        if (!userName.equals("")){
            label_err1.setText("");
        }
        if (!password.equals("")){
            label_err2.setText("");
        }
        
        String kind = "";
        if (userName.compareTo("")!=0 && password.compareTo("")!=0){
           kind = checkAccount(userName, password);
        }
        switch(kind){
            case "staff":
                this.setVisible(false);
                new Home(userName,kind).setVisible(true);
                break;
             
            case "none":
                JOptionPane.showMessageDialog(this, "tài khoản không tồn tại hoặc mật khẩu không đúng","Inane Eror",JOptionPane.ERROR_MESSAGE);
                break;
        }
    }//GEN-LAST:event_btn_loginActionPerformed

