    public boolean searchSDT(String sdt){
        Connection ketNoi = DatVeXe.layKetNoi();
        String sql = "select Passenger_SDT from Ticket where Passenger_SDT = '"+sdt+"'";
        try {
            PreparedStatement ps = ketNoi.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            if (!rs.next()) return false;
            else return true;
        } catch (Exception e) {
            System.out.println("Loi searchSDT"+e);
        }
        return true;
    }
    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        String sdt = txt_sdt.getText();
        if (sdt.equals("")){
            JOptionPane.showMessageDialog(this,"Hãy nhập sdt của hành khách muốn hủy vé!" ,"Inane Eror",JOptionPane.ERROR_MESSAGE);
        }else{
            boolean checkSdt = searchSDT(sdt);
            if (checkSdt == false){
                JOptionPane.showMessageDialog(this,"SDT không đúng hoặc chưa đặt vé!" ,"Inane warning",JOptionPane.WARNING_MESSAGE);
            }else{
                this.setVisible(false);
                CancelTicket c = new CancelTicket(sdt);
                c.setLocationRelativeTo(null);
                c.setVisible(true);
            }
        }
    }//GEN-LAST:event_btn_searchActionPerformed

